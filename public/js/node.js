/* Definicion de la clase Nodo - nodo*/
function CustomNode(data){
	this.id = data.info.identificacion
	this.nomb = data.info.nombre
	this.apelli = data.info.apellido
	this.not1 = data.info.nota1
	this.not2 = data.info.nota2
	this.not3 = data.info.nota3
	this.notfinal = data.info.notadefinitiva
	this.sig = data.info.sig
	this.ant = data.info.ant
}
